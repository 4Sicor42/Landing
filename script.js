var btn = document.getElementById("dark-theme-button");
var link = document.getElementById("theme-link");

btn.addEventListener("click", function () {
  ChangeTheme();
});

function setTheme(theme) {
  localStorage.setItem("theme", theme);
}

function applyThemeOnLoad() {
  const storedTheme = localStorage.getItem("theme");
  const storedLanguage = localStorage.getItem("language");
  if (storedTheme) {
    const link = document.getElementById("theme-link"); 
    link.setAttribute("href", storedTheme);
  }
  if (storedLanguage) {
    // Примените язык к текстовым элементам на странице
    applyLanguage(storedLanguage);
  }
}

function applyLanguage(language) {
  const elementsToTranslate = document.querySelectorAll("[data-i18]");
  
  elementsToTranslate.forEach((element) => {
    const key = element.getAttribute("data-i18");
    element.textContent = i18Obj[language][key] || "";
  });

  // Сохраните выбранный язык в локальном хранилище
  localStorage.setItem("language", language);
}


document.getElementById("languageButtonEN").addEventListener("click", function () {
  applyLanguage("en");
});

document.getElementById("languageButtonRU").addEventListener("click", function () {
  applyLanguage("ru");
});


