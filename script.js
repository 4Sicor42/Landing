function ChangeTheme() {
  let lightTheme = "./style.css";
  let darkTheme = "./dark.css";

  var currTheme = link.getAttribute("href");
  var theme = "";

  if (currTheme == lightTheme) {
    currTheme = darkTheme;
    theme = "dark";
  } else {
    currTheme = lightTheme;
    theme = "light";
  }

  setTheme(currTheme); 
  link.setAttribute("href", currTheme);
}

